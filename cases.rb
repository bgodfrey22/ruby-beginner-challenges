weather = clear

case weather
when "sunny"
    puts "Grab your sunglasses and do something outside today."
when "cloudy"
    puts "Bring an umbrella just in case it rains."
when "foggy"
    puts "Not the clearest weather, so be careful driving."
when "rainy"
    puts "Find an indoor activity or wear a raincoat and rainboots."
when "snowy"
    puts "Time for some hot chocolate and a good movie."
else puts "Take your chances."
end